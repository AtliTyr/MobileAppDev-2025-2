// Типы фигур Тетриса
export type TetrominoType = 'I' | 'O' | 'T' | 'S' | 'Z' | 'J' | 'L';

export interface Position {
  x: number;
  y: number;
}

export interface Tetromino {
  type: TetrominoType;
  shape: number[][];
  position: Position;
  color: string;
}

export type BoardCell = string | null;
export type GameBoard = BoardCell[][];

export interface GameState {
  board: GameBoard;
  currentPiece: Tetromino | null;
  nextPieces: Tetromino[];
  heldPiece: Tetromino | null;
  canHold: boolean;
  score: number;
  level: number;
  lines: number;
  isPaused: boolean;
  isGameOver: boolean;
  gameSpeed: number;
}

